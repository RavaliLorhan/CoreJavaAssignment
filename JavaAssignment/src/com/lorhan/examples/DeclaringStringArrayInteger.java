package com.lorhan.examples;

public class DeclaringStringArrayInteger {

	public static void main(String[] args) {

		// Declaring a String
		String name;
		name = "Java";

		// Declaring an Integer;
		int num;
		num = 22;

		// Declaring an Array
		int[] arr;
		arr = new int[3];
		arr[0] = 2;
		arr[1] = 22;
		arr[2] = 20;
		
		//printing the values
		System.out.println("Name = "+name);
		System.out.println("Num = "+num);
		System.out.println("arr = "+arr[2]);

	}

}
